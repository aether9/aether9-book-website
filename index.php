		<h2>about this book</h2>
		
		<div class="product-shot cbox">
			<img src="img/products/product-shot-01.jpg" alt="the cover of the Aether9 book" />
		</div>
	
		<p><strong>aether9 - <span class="smaller">REMOTE REALTIME STORYTELLING</span></strong> is a print publication based on the archives of the aether9 project.</p>
		
		<p>Aether9 is a collaborative adventure exploring the danger zones of networked audio-visual live performance. This catalogue of their work was generated from the collection of images, mailing-list and chat interactions that developed between 2007 – 2010. The process, behind-the-scenes discussions and perils of networked communication, technical pitfalls, and script development, can thus be followed from performance to performance.</p>
		
		<p>In addition, the book can be navigated (non-linearly) by following selected paths (TRACEROUTES) based on underlining themes, interests and whims of the aether9 project.</p>
		
		<p>This book was designed during an intense sprint and some after shocks from June to September 2012, in collaboration with the Open Source Publishing team.</p>
	
